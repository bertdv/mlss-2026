begin
	
	function calibrate_science_image(image::Array, noise::Distribution)
		# We remove all values below a 90% noise threshold.
		cutoff = quantile(Poisson(mode(noise)), 0.990)

		# Let's keep it simple for now :)
		T = eltype(image)
		max.(zero(T), image .- round(T, cutoff))
	end

	"""
	```julia
	calibrate_science_image(image::Array, noise::Distribution)::Array
	```

	Remove background noise from `image`, using the provided model for `noise`.
	"""
	function calibrate_science_image(image::Array, noise::Real)
		calibrate_science_image(image, Poisson(noise))
	end

end

# ‚ïî‚ïê‚ï° 800bafda-091a-4d10-841a-d3e7ac9fe777
"""
```julia
bins_to_observations(image::AbstractArray{<:Integer})::Vector
```

Turn an array of binned data (histogram values) into a representative vector of individual observations. The output type is:
- `Vector{Float64}` if `image` is a `Vector` (1D)
- `Vector{Vector{Float64}}` if `image` is a matrix or higher-dimensional. In this case, each element is a coordinate, with a length equal to the number of dimensions of `image`.


The result is chosen such that a histogram of the result would return the original binned image. The length of the result is `sum(image)`.

Synthetic uniform random noise `Uniform(-.5,.5)` is added to each coordinate.
"""
function bins_to_observations(image::AbstractArray{<:Integer})::Vector
	rng = StableRNG(42)
	Iterators.flatmap(pairs(IndexCartesian(), image)) do (coord, val)
		# reverse to go from (Row,Column) index to (x,y)
		c = reverse(Tuple(coord))
		
		# add random noise within the 1x1 pixel.
		out(_i) = if image isa Vector
			only(c) + rand(rng, Uniform(-.5,.5))
		else
			c .+ [rand(rng, Uniform(-.5,.5)) for _ in c]
		end
		
		map(out, 1:val)
	end |> collect
end

