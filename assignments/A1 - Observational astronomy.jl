_3 points_


The CCD circuit uses a digital amplifier, to measure photons with very high sensitivity. This setup is able to pick up individual photons, but there is also noise introduced in the amplifier gain process. This gain noise (aka _thermal noise_ or _dark current_) is a constant background level of noise that is added to every pixel's reading.

Because the level of gain noise is temperature-dependent, it is important to calibrate this for every study. Usually, before making observations (i.e. taking star pictures), you take some calibration images:
1. **Dark frame** - you take a picture of the same exposure time as your observation (e.g. 60 seconds), but **with the lid on** the telescope. This means no star light is measured, only CCD noise.
2. **Flat field** _(not in this assignment)_ ‚Äì you take a picture of a uniform light source, e.g. the inside of the telescope dome with the lights on, a picture of the sky taken during twilight (so-called _sky flat_). This measures pixel-to-pixel sensitivity variations and vignetting (uneven illumination).
3. **Science frame** ‚Äì the actual image of the celestial object.
